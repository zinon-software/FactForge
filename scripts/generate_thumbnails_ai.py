def main():
    target = sys.argv[1] if len(sys.argv) > 1 else None

    if target:
        items = [(target, THUMBNAILS[target])] if target in THUMBNAILS else []
        if not items:
            print(f"❌ Unknown ID: {target}")
            return
    else:
        items = list(THUMBNAILS.items())

    print(f"\n🎨 Generating {len(items)} AI-powered thumbnails (Pollinations.ai Flux)\n")

    success = 0
    for vid_id, cfg in items:
        try:
            make_thumbnail_ai(vid_id, cfg)
            success += 1
        except Exception as e:
            print(f"  ❌ {vid_id}: {e}")
        time.sleep(1)  # polite delay between API calls

    print(f"\n✅ {success}/{len(items)} thumbnails generated")


if __name__ == "__main__":
    main()
