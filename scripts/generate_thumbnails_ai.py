def get_font(size):
    for path in [
        "/System/Library/Fonts/Supplemental/Impact.ttf",
        "/System/Library/Fonts/Supplemental/Arial Bold.ttf",
        "/System/Library/Fonts/Helvetica.ttc",
    ]:
        if os.path.exists(path):
            try:
                return ImageFont.truetype(path, size)
            except:
                pass
    return ImageFont.load_default()


def hex_rgb(h):
    h = h.lstrip('#')
    return tuple(int(h[i:i+2], 16) for i in (0, 2, 4))


def outline_text(draw, xy, text, font, fill, outline, width=6):
    x, y = xy
    for dx in range(-width, width+1, 2):
        for dy in range(-width, width+1, 2):
            if dx or dy:
                draw.text((x+dx, y+dy), text, font=font, fill=outline, anchor="mm")
    draw.text(xy, text, font=font, fill=fill, anchor="mm")


def fetch_ai_background(prompt, width, height, seed=42, retries=3):
    """Download AI-generated background from Pollinations.ai"""
    url = POLLINATIONS_URL.format(
        prompt=requests.utils.quote(prompt),
        w=width, h=height, seed=seed
    )
    for attempt in range(retries):
        try:
            print(f"    🎨 AI background: {prompt[:50]}...", end=" ", flush=True)
            r = requests.get(url, timeout=60)
            if r.status_code == 200 and len(r.content) > 5000:
                img = Image.open(BytesIO(r.content)).convert("RGB")
                img = img.resize((width, height), Image.Resampling.LANCZOS)
                print(f"✓ ({len(r.content)//1024}KB)")
                return img
            else:
                print(f"retry ({r.status_code})")
        except Exception as e:
            print(f"retry ({e})")
        time.sleep(3)
    print("⚠️ fallback to gradient")
    return None


def make_gradient_bg(W, H, bg_rgb, acc_rgb):
    """Fallback: solid gradient background"""
    img = Image.new("RGB", (W, H))
    draw = ImageDraw.Draw(img)
    for y in range(H):
        t = y / H
        r = int(bg_rgb[0] * (1 - t * 0.35))
        g = int(bg_rgb[1] * (1 - t * 0.35))
        b = int(bg_rgb[2] * (1 - t * 0.35))
        draw.line([(0, y), (W, y)], fill=(r, g, b))
    return img


def make_thumbnail_ai(vid_id, cfg):
    is_short = vid_id.startswith("S")
    W, H = (1080, 1920) if is_short else (1280, 720)

    acc_rgb = hex_rgb(cfg["accent"])
    bg_rgb = hex_rgb(cfg["bg"])

    # ── 1. AI Background (or gradient fallback) ──────────────────────
    ai_prompt = cfg.get("ai_prompt", "")
    img = None
    if ai_prompt:
        img = fetch_ai_background(ai_prompt, W, H, seed=cfg.get("seed", 42))

    if img is None:
        img = make_gradient_bg(W, H, bg_rgb, acc_rgb)

    # ── 2. Dark overlay so text is always readable ────────────────────
    overlay = Image.new("RGBA", (W, H), (0, 0, 0, 0))
    od = ImageDraw.Draw(overlay)
    # Semi-transparent black gradient from bottom (where text lives)
    for y in range(H):
        t = y / H
        # Bottom 60% gets darker overlay
        if t > 0.4:
            alpha = int(180 * ((t - 0.4) / 0.6))
        else:
            alpha = int(60 * (1 - t / 0.4))
        od.line([(0, y), (W, y)], fill=(0, 0, 0, alpha))
    img = Image.alpha_composite(img.convert("RGBA"), overlay).convert("RGB")
    draw = ImageDraw.Draw(img)

    # ── 3. Top accent glow blob ───────────────────────────────────────
    blob = Image.new("RGBA", (W, H), (0, 0, 0, 0))
    bd = ImageDraw.Draw(blob)
    for rad in range(min(W, H) // 2, 0, -30):
        alpha = max(0, int(50 * (1 - rad / (min(W, H) // 2))))
        bd.ellipse([W - rad, -rad, W + rad, rad], fill=(*acc_rgb, alpha))
    img = Image.alpha_composite(img.convert("RGBA"), blob).convert("RGB")
    draw = ImageDraw.Draw(img)

    # ── 4. Bold accent bar at top ─────────────────────────────────────
    bar_h = max(8, int(H * 0.013))
    draw.rectangle([0, 0, W, bar_h], fill=acc_rgb)

    # ── 5. MAIN STAT (hero) ───────────────────────────────────────────
    stat = cfg.get("stat", "")
    if stat:
        stat_size = cfg.get("stat_size", int(H * 0.30))
        stat_y = cfg.get("stat_y", int(H * 0.38))
        f_stat = get_font(stat_size)
        # Glow layers
        for glow_r, alpha in [(stat_size // 2, 30), (stat_size // 3, 50), (stat_size // 4, 70)]:
            glow = Image.new("RGBA", (W, H), (0, 0, 0, 0))
            gd = ImageDraw.Draw(glow)
            gd.text((W // 2, stat_y), stat, font=f_stat, fill=(*acc_rgb, alpha), anchor="mm")
            glow = glow.filter(ImageFilter.GaussianBlur(radius=glow_r))
            img = Image.alpha_composite(img.convert("RGBA"), glow).convert("RGB")
        draw = ImageDraw.Draw(img)
        outline_text(draw, (W // 2, stat_y), stat, f_stat, acc_rgb, (0, 0, 0), width=8)

    # ── 6. Line 1 (white bold) ────────────────────────────────────────
    line1 = cfg.get("line1", "").upper()
    if line1:
        l1_size = cfg.get("l1_size", int(H * 0.075))
        l1_y = cfg.get("l1_y", int(H * 0.65))
        f1 = get_font(l1_size)
        outline_text(draw, (W // 2, l1_y), line1, f1, (255, 255, 255), (0, 0, 0), width=6)

    # ── 7. Line 2 (accent color) ──────────────────────────────────────
    line2 = cfg.get("line2", "").upper()
    if line2:
        l2_size = cfg.get("l2_size", int(H * 0.065))
        l2_y = cfg.get("l2_y", int(H * 0.75))
        f2 = get_font(l2_size)
        outline_text(draw, (W // 2, l2_y), line2, f2, acc_rgb, (0, 0, 0), width=5)

    # ── 8. Category pill ─────────────────────────────────────────────
    cat = cfg.get("category", "").upper()
    if cat:
        f_cat = get_font(int(H * 0.028))
        bbox = draw.textbbox((0, 0), cat, font=f_cat, anchor="mm")
        pw = (bbox[2] - bbox[0]) + int(W * 0.05)
        ph = int(H * 0.045)
        px, py = W // 2, int(H * 0.055)
        draw.rounded_rectangle([px - pw // 2, py - ph // 2, px + pw // 2, py + ph // 2],
                                radius=ph // 2, fill=acc_rgb)
        draw.text((px, py), cat, font=f_cat, fill=(0, 0, 0), anchor="mm")

    # ── 9. FACTFORGE watermark ────────────────────────────────────────
    f_wm = get_font(int(H * 0.022))
    wm_y = H - int(H * 0.025)
    draw.text((W // 2, wm_y), "FACTFORGE", font=f_wm,
              fill=(*acc_rgb, 180), anchor="mm")

    # ── 10. Bottom bar ────────────────────────────────────────────────
    draw.rectangle([0, H - bar_h, W, H], fill=acc_rgb)

    # Save
    out = f"{BASE}/output/{vid_id}/thumbnail.jpg"
    os.makedirs(f"{BASE}/output/{vid_id}", exist_ok=True)
    img.save(out, "JPEG", quality=95)
    size_kb = os.path.getsize(out) // 1024
    print(f"  ✅ {vid_id} ({'Short 1080×1920' if is_short else 'Long 1280×720'}) — {size_kb}KB saved")
    return out


